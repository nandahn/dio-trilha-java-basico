public class VerificarNumeros {
    public static void main(String[] args) {
        int numero = 5;

        if (numero > 0) {
            System.out.println("O numero é positivo");
            
        } else if (numero < 0) {
            System.out.println("O numero é negativo");
            
        } else {
            System.out.println("O numero é zero");
        }
    }
}
